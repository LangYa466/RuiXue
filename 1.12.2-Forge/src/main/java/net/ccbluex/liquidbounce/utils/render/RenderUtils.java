    public static void drawRect(float x, float y, float width, float height, boolean nellycolor) {
        // 计算矩形的四个顶点坐标
        float x1 = x;
        float y1 = y;
        float x2 = x + width;
        float y2 = y;
        float x3 = x + width;
        float y3 = y + height;
        float x4 = x;
        float y4 = y + height;

        // 更新顶点数据
        float[] vertices = {
                x1, y1,
                x2, y2,
                x3, y3,
                x4, y4
        };

        // 绑定VBO，并更新顶点数据
        glBindBuffer(GL_ARRAY_BUFFER, NellyRenderUtils.vbo);
        glBufferData(GL_ARRAY_BUFFER, FloatBuffer.wrap(vertices), GL_DYNAMIC_DRAW);
        glBindBuffer(GL_ARRAY_BUFFER, 0);

        // 使用着色器程序
        glUseProgram(NellyRenderUtils.program);

        // 更新uniform变量
        if(nellycolor) {
            glUniform4f(NellyRenderUtils.color1Location, CustomColor.r.get(), CustomColor.g.get(), CustomColor.b.get(), CustomColor.a.get());
            glUniform4f(NellyRenderUtils.color2Location, CustomColor.r.get(), CustomColor.g.get(), CustomColor.b.get(), CustomColor.a.get());
        }

        // 绑定VAO并绘制
        glBindVertexArray(NellyRenderUtils.vao);
        glDrawArrays(GL_QUADS, 0, 4);
        glBindVertexArray(0);

        // 停用着色器程序
        glUseProgram(0);
    }


