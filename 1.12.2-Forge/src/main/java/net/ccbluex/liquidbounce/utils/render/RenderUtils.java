    public void drawOutlineWithShadow(AxisAlignedBB boundingBox, float red, float green, float blue, float alpha, float lineWidth) {
        Tessellator tessellator = Tessellator.getInstance();
        BufferBuilder bufferBuilder = tessellator.getBuffer();
        GlStateManager.pushMatrix();

        // Translate to the center of the bounding box
        double centerX = boundingBox.minX + (boundingBox.maxX - boundingBox.minX) / 2.0;
        double centerY = boundingBox.minY + (boundingBox.maxY - boundingBox.minY) / 2.0;
        double centerZ = boundingBox.minZ + (boundingBox.maxZ - boundingBox.minZ) / 2.0;
        GlStateManager.translate(centerX, centerY, centerZ);

        // Scale to the size of the bounding box
        double width = boundingBox.maxX - boundingBox.minX;
        double height = boundingBox.maxY - boundingBox.minY;
        double depth = boundingBox.maxZ - boundingBox.minZ;
        GlStateManager.scale(width, height, depth);

        // Disable textures and enable blending
        GlStateManager.disableTexture2D();
        GlStateManager.enableBlend();
        GlStateManager.tryBlendFuncSeparate(GL11.GL_SRC_ALPHA, GL11.GL_ONE_MINUS_SRC_ALPHA, GL11.GL_ONE, GL11.GL_ZERO);
        GlStateManager.color(red, green, blue, alpha);

        // Draw the lines without depth test
        GlStateManager.disableDepth();
        bufferBuilder.begin(GL11.GL_LINES, DefaultVertexFormats.POSITION);
        // Top face
        bufferBuilder.pos(-0.5, 0.5, -0.5).endVertex();
        bufferBuilder.pos(0.5, 0.5, -0.5).endVertex();

        bufferBuilder.pos(0.5, 0.5, -0.5).endVertex();
        bufferBuilder.pos(0.5, 0.5, 0.5).endVertex();

        bufferBuilder.pos(0.5, 0.5, 0.5).endVertex();
        bufferBuilder.pos(-0.5, 0.5, 0.5).endVertex();

        bufferBuilder.pos(-0.5, 0.5, 0.5).endVertex();
        bufferBuilder.pos(-0.5, 0.5, -0.5).endVertex();

        // Bottom face
        bufferBuilder.pos(-0.5, -0.5, -0.5).endVertex();
        bufferBuilder.pos(0.5, -0.5, -0.5).endVertex();

        bufferBuilder.pos(0.5, -0.5, -0.5).endVertex();
        bufferBuilder.pos(0.5, -0.5, 0.5).endVertex();

        bufferBuilder.pos(0.5, -0.5, 0.5).endVertex();
        bufferBuilder.pos(-0.5, -0.5, 0.5).endVertex();

        bufferBuilder.pos(-0.5, -0.5, 0.5).endVertex();
        bufferBuilder.pos(-0.5, -0.5, -0.5).endVertex();

        // Vertical lines
        bufferBuilder.pos(-0.5, 0.5, -0.5).endVertex();
        bufferBuilder.pos(-0.5, -0.5, -0.5).endVertex();

        bufferBuilder.pos(0.5, 0.5, -0.5).endVertex();
        bufferBuilder.pos(0.5, -0.5, -0.5).endVertex();

        bufferBuilder.pos(0.5, 0.5, 0.5).endVertex();
        bufferBuilder.pos(0.5, -0.5, 0.5).endVertex();

        bufferBuilder.pos(-0.5, 0.5, 0.5).endVertex();
        bufferBuilder.pos(-0.5, -0.5, 0.5).endVertex();
        tessellator.draw();
        GlStateManager.enableDepth();

        // Draw the shadow with depth test
        GlStateManager.disableTexture2D();
        GlStateManager.color(0, 0, 0, alpha);
        GlStateManager.depthFunc(GL11.GL_LEQUAL);

        bufferBuilder.begin(GL11.GL_QUADS, DefaultVertexFormats.POSITION);
        // Top face
        bufferBuilder.pos(-0.5 - lineWidth / width, 0.5 + lineWidth / height, -0.5 - lineWidth / depth).endVertex();
        bufferBuilder.pos(0.5 + lineWidth / width, 0.5 + lineWidth / height, -0.5 - lineWidth / depth).endVertex();
        bufferBuilder.pos(0.5 + lineWidth / width, 0.5 + lineWidth / height, 0.5 + lineWidth / depth).endVertex();
        bufferBuilder.pos(-0.5 - lineWidth / width, 0.5 + lineWidth / height, 0.5 + lineWidth / depth).endVertex();

        // Bottom face
        bufferBuilder.pos(-0.5 - lineWidth / width, -0.5 - lineWidth / height, -0.5 - lineWidth / depth).endVertex();
        bufferBuilder.pos(0.5 + lineWidth / width, -0.5 - lineWidth / height, -0.5 - lineWidth / depth).endVertex();
        bufferBuilder.pos(0.5 + lineWidth / width, -0.5 - lineWidth / height, 0.5 + lineWidth / depth).endVertex();
        bufferBuilder.pos(-0.5 - lineWidth / width, -0.5 - lineWidth / height, 0.5 + lineWidth / depth).endVertex();

        // Left face
        bufferBuilder.pos(-0.5 - lineWidth / width, -0.5 - lineWidth / height, 0.5 + lineWidth / depth).endVertex();
        bufferBuilder.pos(-0.5 - lineWidth / width, 0.5 + lineWidth / height, 0.5 + lineWidth / depth).endVertex();
        bufferBuilder.pos(-0.5 - lineWidth / width, 0.5 + lineWidth / height, -0.5 - lineWidth / depth).endVertex();
        bufferBuilder.pos(-0.5 - lineWidth / width, -0.5 - lineWidth / height, -0.5 - lineWidth / depth).endVertex();

        // Right face
        bufferBuilder.pos(0.5 + lineWidth / width, -0.5 - lineWidth / height, 0.5 + lineWidth / depth).endVertex();
        bufferBuilder.pos(0.5 + lineWidth / width, 0.5 + lineWidth / height, 0.5 + lineWidth / depth).endVertex();
        bufferBuilder.pos(0.5 + lineWidth / width, 0.5 + lineWidth / height, -0.5 - lineWidth / depth).endVertex();
        bufferBuilder.pos(0.5 + lineWidth / width, -0.5 - lineWidth / height, -0.5 - lineWidth / depth).endVertex();

        tessellator.draw();
        GlStateManager.enableTexture2D();

        // Disable blending and enable textures
        GlStateManager.disableBlend();
        GlStateManager.enableTexture2D();
        GlStateManager.popMatrix();
    }

